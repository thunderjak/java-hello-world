public class RelationOperatorTest {
 
  public static void main(String[] args) {
     int a = 10;
     int b = 20;
     System.out.println("a = "+a+",b = "+b);
     System.out.println("a == b = " + (a == b) );
     System.out.println("a != b = " + (a != b) );
     System.out.println("a > b = " + (a > b) );
     System.out.println("a < b = " + (a < b) );
     System.out.println("b >= a = " + (b >= a) );
     System.out.println("b <= a = " + (b <= a) );
     System.out.println("b == a*2 = " + (b == a*2) );

  }
}
